
public class School {
 private int anzahl;
 private String name;
public School(int anzahl, String name) {
	super();
	this.anzahl = anzahl;
	this.name = name;
}
public int getAnzahl() {
	return anzahl;
}
public void setAnzahl(int anzahl) {
	this.anzahl = anzahl;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
 
}
